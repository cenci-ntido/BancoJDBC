    private MateriaPrima getMateriaPrima() {
        MateriaPrima materiaPrima = new MateriaPrima();
        materiaPrima.setDescricao(tfDescricao.getText());
        materiaPrima.setUnidade(tfUnidade.getText());
        materiaPrima.setSaldo(Float.valueOf(tfSaldo.getText()));
        return materiaPrima;
    }
    private int linhaSelecionada;
    private MateriaPrimaDao materiaPrimaDao;
    private MateriaPrimaListModel materiaPrimaListModel;
    private boolean edit = false;

    private void save() {
        MateriaPrima materiaPrima = getMateriaPrima();
        materiaPrimaDao = new MateriaPrimaDao();
        if (!edit) {
            materiaPrimaDao.insert(materiaPrima);
            materiaPrimaListModel.insertModel(materiaPrima);
            this.dispose();
        } else {
            materiaPrima.setId(Integer.parseInt(tfCodigo.getText()));
            materiaPrimaDao.update(materiaPrima);
            materiaPrimaListModel.atualizarModel(linhaSelecionada, materiaPrima);
            this.dispose();
        }
    }
